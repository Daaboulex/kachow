#!/usr/bin/env node

// Generates DIRECTORY-MAP.md from the directory structure
// Usage: node scripts/generate-directory-map.mjs [--root <path>]
// Defaults to the parent of the scripts/ directory as root.

import { readdirSync, statSync, writeFileSync } from 'node:fs';
import { join, dirname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootArg = process.argv.indexOf('--root');
const root = rootArg !== -1 ? process.argv[rootArg + 1] : join(__dirname, '..');

const SKIP = new Set([
  '.git', 'node_modules', 'runtime', '.stversions', '.stfolder', '.obsidian',
]);

const DESCRIPTIONS = {
  'core': 'Global shared resources — memory, skills, commands',
  'core/memory': 'Global memories (non-project-specific)',
  'core/memory/user': 'User profile memories',
  'core/memory/feedback': 'Global feedback memories',
  'core/memory/archive': 'Archived memories (never deleted)',
  'core/memory/project': 'Project-level global memories',
  'core/memory/reference': 'Reference pointers to external systems',
  'core/skills': 'User-authored global skills',
  'core/skills/commands': 'Slash-command skills (cmd-* shims)',
  'core/skills/global': 'Reusable global skills',
  'core/commands': 'Slash commands shared across tools',
  'modules': 'Tool adapters and hooks',
  'modules/tools': 'Per-tool adapter configs (capabilities.yaml, symlinks.yaml)',
  'modules/hooks': 'Hook source + MANIFEST.yaml (15 hooks)',
  'modules/hooks/src': 'Hook implementation files',
  'modules/hooks/lib': 'Shared hook utilities (28 files)',
  'projects': 'Per-project state, memory, skills',
  'projects/template': 'Template for new project .ai-context/ directories',
  'generated': 'Generated files — never hand-edit',
  'generated/configs': 'Tool settings (JSON, TOML, TypeScript) + kachow-bridge.ts',
  'generated/sync': 'Sync policy source (policy.yaml → .gitignore + .stignore)',
  'scripts': 'Generation, verification, and migration scripts',
  'scripts/publish-assets': 'Public kachow framework docs (scrubbed for release)',
  'runtime': 'Cache, sessions, telemetry — excluded from sync',
  'runtime/self-improvement': 'Self-improvement finding queue',
  'runtime/session-exports': 'Auto-exported session HTML transcripts (per-machine)',
  'public': 'Public-facing outputs (gitignored)',
  'public/kachow-mirror': 'Scrubbed public release (regenerated from scrub pipeline)',
  'handoffs': 'Session handoff state',
  'handoffs/sessions': 'Per-session handoff files',
  'handoffs/archive': 'Archived handoffs by project',
  'handoffs/deferred': 'Deferred work items across sessions',
  'docs': 'Internal documentation (not published)',
  'docs/plans': 'Implementation plans',
  'docs/superpowers': 'Superpowers plugin artifacts',
  'docs/superpowers/specs': 'Design specs from brainstorming sessions',
  'mcp': 'MCP server (not currently active)',
};

function scanDirs(dir, depth = 0, maxDepth = 3) {
  if (depth > maxDepth) return [];
  const results = [];
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return [];
  }
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    if (SKIP.has(entry.name)) continue;
    if (entry.name.startsWith('.')) continue;
    const full = join(dir, entry.name);
    const rel = relative(root, full);
    const desc = DESCRIPTIONS[rel];
    if (desc) {
      results.push({ path: rel, desc });
    }
    results.push(...scanDirs(full, depth + 1, maxDepth));
  }
  return results;
}

const dirs = scanDirs(root);

// Add dotdirs that scanDirs skips but are important
const DOTDIRS = {
  '.agents/skills': 'Plugin-synced skills — cross-tool discovery (Gemini/Codex/Pi)',
  '.superpowers': 'Superpowers plugin workspace (brainstorm sessions, visual companion)',
};
for (const [rel, desc] of Object.entries(DOTDIRS)) {
  if (statSync(join(root, rel), { throwIfNoEntry: false })?.isDirectory()) {
    dirs.push({ path: rel, desc });
  }
}

// Add runtime (skipped by scanner but documented)
if (statSync(join(root, 'runtime'), { throwIfNoEntry: false })?.isDirectory()) {
  for (const [rel, desc] of Object.entries(DESCRIPTIONS)) {
    if (rel.startsWith('runtime') && !dirs.find(d => d.path === rel)) {
      dirs.push({ path: rel, desc });
    }
  }
}

// Sort by path
dirs.sort((a, b) => a.path.localeCompare(b.path));

const now = new Date().toISOString().slice(0, 10);

const lines = [
  '# Directory Map',
  '',
  '<!-- Auto-generated by scripts/generate-directory-map.mjs — do not edit directly -->',
  `<!-- Last generated: ${now} -->`,
  '',
  '| Path | Purpose |',
  '|------|---------|',
];

for (const { path, desc } of dirs) {
  lines.push(`| \`${path}/\` | ${desc} |`);
}

lines.push('');

writeFileSync(join(root, 'DIRECTORY-MAP.md'), lines.join('\n'), 'utf8');
console.log(`Generated DIRECTORY-MAP.md (${dirs.length} directories)`);
